#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tab;
	int		i;

	if (!(tab = (char*)malloc(sizeof(char) * (ft_strlen(s1) + 1))))
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		tab[i] = s1[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
